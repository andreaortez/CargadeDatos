    private void bt_buscarSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_buscarSMouseClicked
        try {
            JFileChooser jfc = new JFileChooser();
            int seleccion = jfc.showOpenDialog(this);
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                secciones = jfc.getSelectedFile();
                tf_FilepathS.setText(secciones.getName());

            } else {
                JOptionPane.showMessageDialog(this, "¡Archivo no soportado!", "Warning", WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_bt_buscarSMouseClicked

